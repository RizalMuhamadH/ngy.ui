<b>1. Apakah Nagoya Nipoindo itu?</b> 

<br>

<div style="margin-left:19px">Jasa pengiriman paket barang dari Jepang ke Indonesia.</div>

<br>



<b>2. Apa kelebihan Nagoya Nipoindo dengan jasa pengiriman yang  lain?</b>

<br>

<div style="margin-left:19px">Kami menyediakan dus gratis S, M, L dan LL. Kami melayani dengan pelayanan maksimal, mengutamakan kemudahan,  kenyamanan, keamanan dan amanah. Seluruh pengiriman barang dari kami bergaransi tanpa biaya tambahan.</div>

<br>



<b>3. Barang apa saja yang boleh dikirim melalui Nagoya Nipoindo?</b>

<br>

<div style="margin-left:19px">Barang barang pribadi non komersil seperti: pakaian, sepatu, alat elektronik, mainan, buku, hiasan dan lain lain.</div>

<br>



<b>4. Barang apa saja yang tidak boleh dikirim?</b>

<br>

<div style="margin-left:19px">Kendaraan bermotor, mesin dan sparepart-nya,  Barang yang mudah terbakar, alkohol, obat-obatan terlarang, unsur pornografi, Rokok, Senjata api, senjata tajam  barang dagangan.</div>

<br>



<b>5. Bagaimana cara mengirimkan barang melalui Nagoya Nipoindo?</b>

<br>

<div style="margin-left:19px">Siapkan dus (bisa dus sendiri atau dus gratis dari kami) bisa juga koper dan storage/box plastik. Untuk dus selain dus yang tersedia dari kami, silahkan konsultasikan ke kami ukurannya untuk penentuan biaya.</div>

<br>



<b>6. Bagaimana cara pengisian data dan packng list?</b>

<br>

<div style="margin-left:19px">Isi data tulis nama pengirim, nama penerima, alamat lengkap di Indonesia beserta 2 (dua) no.telpon. Kemudian ditempel di dus, koper atau stoarge / eco box. Untuk packing list, tulis isi barang didalam dus apa saja beserta jumlahnya. Masukan kedalam amplop ditempel diluar dus yang mudah dicabut.</div>

<br>



<b>7. Bagimana cara mendapatkan packing list?</b>

<br>

<div style="margin-left:19px">silakan download di website, atau bisa mendapatkannya pada saat permintaan dus dari kami. Packing list pada saat penerimaan dus terdiri dari 3 lembar dan pengirim diberikan salinan 1 lembar sebagai bukti pengiriman barang.</div>

<br>



<b>8. Apakah data diri perlu disertakan?</b>

<div style="margin-left:19px">Iya, harus menyertakan foto copy paspor halaman depan dan halaman visa serta zaryou kado (ID Card  Jepang) bagian depan dan belakang</div>

<br>



<b>9. Berapa biaya ongkos kirim dari Jepang ke Indonesia?</b>

<div style="margin-left:19px">Biaya ongkir kami hitung berdasarkan ukuran dimensi dus bukan berat. Dus yang tersedia dari kami : Dus S 짜7000 M 짜11,000 L 짜15,000, LL 17,000. Diluar ukuran itu diukur dengan rumus :  Px L x T x 0,110 = 짜 
Dan bagi yang mengirimkan ke luar Jawa Madura. Ada biaya tambahan antar pulau, silakan konsultasikan kepada kami. </div> 

<br>

<b>10. Bagaimana cara mengirimkan barang ke kantor kami?</b>

<br>

<div style="margin-left:19px">Bagi wilayah Aichiken, Hamamatsu, Gifuken kami akan menjemput langsung, diluar wilayah itu bisa dikirim via SAGAWA. Untuk biaya pengirimannya akan kami subsidi setengah dari harga ongkirnya. (stengah ongkir SAGAWA tersebut akan dikurangi dari biaya ongkir ke Indonesianya pada saat transfer) Untuk penjemputan SAGAWA silahkan konsultasikan ke kami sesuai keinginan waktunya.</div>

<br>



<b>11. Bagaimana cara pengepakan yang benar dan aman? Dan berapa maksimal berat?</b>

<br>

<div style="margin-left:19px">Silahkan memilih dus / koper / storage sesuai keinginan, untuk barang elektronik dan pecah belah harap membungkusnya dengan plastik bentol (bubble plastic- terdapat di 100 shop dan home center) agar barang tetap aman dan utuh, tdk mudah rusak / pecah. Seluruh dus harus dilakban bening seluruhnya, untuk koper tidak boleh dikunci dan akan dibungkus wrapping di kantor kami. Berat maksimal untuk dus S sebaiknya 15kg, M 35kg dan L / LL 40kg. jika berat dus lebih dari berat diatas, mohon konsultasikan terlebih dahulu (khususnya diluar Aichiken, Hamamatsu, Kuwana, Yokkaichi, Gifu, Shiga, akan dikirim melalui SAGAWA yang membatasi berat maksimal 50kg).</div>

<br>



<b>12. Bagaimana cara pembayaran ongkos kirim ke Indonesia?</b>

<br>

<div style="margin-left:19px">Setelah barang sampai di kantor Nagoya, kami akan mengukur dan menghitung biaya,

kemudian akan kami informasikan total tagihan biaya melalui Line / Facebook / Email yang telah ditulis di packing list.

Silakan mentransfer biaya melalui rekening JP post atau MUFJ Bank yang sudah kami tentukan</div>

<br>



<b>13. Berapa lama waktu pengiriman barang sampai Indonesia?</b>

<br>

<div style="margin-left:19px">Untuk Pulau Jawa dan Madura umumnya adalah 35 hari (terhitung dari keberangkatan kapal) dan diluar Jawa 50-60 hari.</div>



 

            

